# QUE ES LA SERIALIZATION?
# ====================================================================================
#
# La serialización es el proceso de convertir un objeto o estructura de datos en un formato plano
#
# Consiste en guardar en un fichero externo una coleccion, un diccionario o un objeto codificado en codigo binarios osea en una succesion de bytes
#
# Esto permite que la distribucion por internet sea mas facil ya que esta en codigo binario
# ====================================================================================
#
# Para poder llevar a cabo este proceso en Python debemos usar una bibloteca en Python que es Pickle
#
# Los metodos mas importantes que usaremos en esta ocasion es
#
# dump() es volcado de datos al fichero binario externo
#
# load() es carga de los datos del fichero binario externo