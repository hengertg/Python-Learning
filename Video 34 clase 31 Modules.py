# MODULES
# ==========================================
# Que son?
#
# Un archivo con extesion .py .pyc(Python compilado) o archivo escrito en C para Cpython,
# que posee su propio espacio de nombres y que puede contener variables, funciones, clases
# e incluso otros modulos.
#
# Para que sirven?
#
# Para organizar y reutilizar el codigo (modularizacion y reutilizacion)
#
# Como se crea un modulo?
#
# Tan sencillo como crear un archivo con extension .py (o pyc o archivo C) y guardarlo donde nos interese
