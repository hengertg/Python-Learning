# Objetivo que tiene trabajar con archivos externos?
#
# Objetivo: persistencia de datos
#
# Es la posibilidad de guardar los datos que has estados utilizando en esa aplicacion python que has estado trabajando para que no se pierdan
#
# Esto hara que cuando uno cierre el archivo se quede guardado y no se pierda
#
# Por ejemplo que cuando haciamos un cambio de una viriable cuando se cerraba la aplicacion volvia a su estado original en ves de guardarse
# cuano lo volvieramos abrir.
# ====================================================================================
#
# Hay dos alternativas donde puedos guardar estas informacion
#
# Manejo de archivos externos
#
# Trabajo con BBDD
# ====================================================================================
#
# A la hora de guardar los archivos con archivos externos hay muchos tipos
#
# Archivos binarios
#
# Archivos de texto planos
#
# Entre otros
#
# En esta ocasion usaremos archivos de texto planos
# ====================================================================================
